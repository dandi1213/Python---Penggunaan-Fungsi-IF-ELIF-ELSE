# #Percabangan/Kondisi If

kecepatan = int(input("Berapa Kecepatan Anda: "))

if kecepatan <= 60:
    print("Sangat Lambat")
elif kecepatan <= 80:
    print("Normal")
elif kecepatan <= 100:
    print("Cukup Cepat")
elif kecepatan <= 120:
    print("Cepat")
elif kecepatan <= 180:
    print("Sangat Cepat")
else:
    print("SANGAT CEPAT SYEKALI ABANGKUH")
