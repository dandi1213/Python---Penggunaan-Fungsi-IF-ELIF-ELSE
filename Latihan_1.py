nilai = input("Nilai saya adalah :")

if nilai == 'A':
    print("IP saya 4.0")
elif nilai == "B":
    print("IP saya 3.0")
elif nilai == "C":
    print("IP saya 2.0")
elif nilai == "D":
    print("IP saya 1.0")
else :
    print("Saya tidak Lulus")

